static int hss_key_eq_test(void)
{
    int ret = 0;
    EVP_PKEY *key[4] = { NULL, NULL, NULL, NULL };
#ifndef OPENSSL_NO_EC
    EVP_PKEY *eckey = NULL;
#endif
    if (!TEST_ptr(key[0] = hsspubkey_from_data(pub1, sizeof(pub1)))
        || !TEST_ptr(key[1] = hsspubkey_from_data(pub1, sizeof(pub1)))
        || !TEST_ptr(key[2] = key_decode_from_data(pub1, sizeof(pub1), NULL))
        || !TEST_ptr(key[3] = hsspubkey_from_data(pub2, sizeof(pub2))))
        goto end;

    ret = TEST_int_eq(EVP_PKEY_eq(key[0], key[1]), 1)
          && TEST_int_eq(EVP_PKEY_eq(key[0], key[2]), 1)
          && TEST_int_ne(EVP_PKEY_eq(key[0], key[3]), 1);
    if (ret == 0)
        goto end;
#ifndef OPENSSL_NO_EC
    if (!TEST_ptr(eckey = EVP_PKEY_Q_keygen(libctx, propq, "EC", "P-256")))
        goto end;
    ret = TEST_int_ne(EVP_PKEY_eq(key[0], eckey), 1);
    EVP_PKEY_free(eckey);
#endif
end:
    EVP_PKEY_free(key[3]);
    EVP_PKEY_free(key[2]);
    EVP_PKEY_free(key[1]);
    EVP_PKEY_free(key[0]);
    return ret;
}

static int hss_pkey_sign(void)
{
    int ret = 0;
    EVP_PKEY_CTX *genctx = NULL;
    EVP_MD_CTX *signctx = NULL;
    EVP_PKEY *key = NULL;
    OSSL_PARAM params[4];
    uint32_t levels = 1;
    uint32_t lms_type = 5;
    uint32_t ots_type = 4;
    unsigned char sig[2048];
    size_t siglen = sizeof(sig);
    unsigned char msg[] = "ABC";

    if (!TEST_ptr(genctx = EVP_PKEY_CTX_new_from_name(libctx, "HSS", propq))
            || !TEST_int_eq(EVP_PKEY_keygen_init(genctx), 1))
        goto err;

    params[0] = OSSL_PARAM_construct_uint32(OSSL_PKEY_PARAM_HSS_LEVELS, &levels);
    params[1] = OSSL_PARAM_construct_uint32(OSSL_PKEY_PARAM_HSS_LMS_TYPE_L1,
                                            &lms_type);
    params[2] = OSSL_PARAM_construct_uint32(OSSL_PKEY_PARAM_HSS_OTS_TYPE_L1,
                                            &ots_type);
    params[3] = OSSL_PARAM_construct_end();
    if (!TEST_ptr(EVP_PKEY_generate(genctx, &key) <= 0))
        goto err;

    if (!TEST_ptr(signctx = EVP_MD_CTX_new())
        || !TEST_int_eq(EVP_DigestSignInit_ex(signctx, NULL, NULL, libctx, propq,
                                              pkey, NULL), 1)
        || !TEST_int_eq(EVP_DigestSign(signctx, sig, &siglen, msg, sizeof(msg)), 1))
        goto err;
    ret = 1;
err:
    EVP_MD_CTX_free(signctx);
    EVP_PKEY_CTX_free(genctx);
    return ret;
}

int setup_tests(void)
{
    OPTION_CHOICE o;
    char *config_file = NULL;
    char *pubfilename = NULL, *sigfilename = NULL;

    /* Swap the libctx to test non-default context only */
    propq = "provider=default";

    while ((o = opt_next()) != OPT_EOF) {
        switch (o) {
        case OPT_PUB:
            pubfilename = opt_arg();
            break;
        case OPT_SIG:
            sigfilename = opt_arg();
            break;
        case OPT_CONFIG_FILE:
            config_file = opt_arg();
            propq = "";
            break;
        case OPT_TEST_CASES:
           break;
        default:
        case OPT_ERR:
            return 0;
        }
    }

    if (!test_get_libctx(&libctx, &nullprov, config_file, &libprov, NULL))
        return 0;

    if (!OSSL_set_max_threads(libctx, 16))
        return 0;
    if (pubfilename != NULL && sigfilename != NULL) {
        if (!TEST_ptr(pubkey = load_key(pubfilename)))
            return 0;
        sigdatalen = load_file(sigfilename, &sigdata);
        if (!TEST_int_gt(sigdatalen, 0))
            return 0;

        ADD_ALL_TESTS(hss_verify_hss_file_test, 2);
    } else {
        ADD_ALL_TESTS(hss_verify_update_test, OSSL_NELEM(testdata));
        ADD_ALL_TESTS(hss_verify_oneshot_test, OSSL_NELEM(testdata));
        ADD_ALL_TESTS(hss_verify_pq_test, OSSL_NELEM(testdata));
        ADD_TEST(hss_verify_bad_sig_test);
        ADD_TEST(hss_verify_bad_sig_len_test);
        ADD_TEST(hss_verify_bad_pub_sig_test);
        ADD_TEST(hss_bad_pub_len_test);
        ADD_TEST(hss_decode_fail_test);
        ADD_TEST(hss_pubkey_decoder_fail_test);
        ADD_TEST(hss_key_eq_test);
        ADD_TEST(hss_key_validate_test);
        ADD_TEST(hss_pkey_verify_fail_test);
        ADD_TEST(hss_pkey_sign);
    }
    return 1;
}

void cleanup_tests(void)
{
    EVP_PKEY_free(pubkey);
    OPENSSL_free(sigdata);
    OSSL_PROVIDER_unload(nullprov);
    OSSL_PROVIDER_unload(libprov);
    OSSL_LIB_CTX_free(libctx);
}
