static int hss_verify_bad_sig_test(void)
{
    int ret = -1, i;
    LMS_VERIFY_DATA *td = &testdata[1];
    EVP_MD_CTX *ctx = NULL;
    EVP_PKEY *pkey = NULL;
    unsigned char *sig = NULL;
    /*
     * Corrupt every 3rd byte to run less tests. The smallest element of an XDR
     * encoding is 4 bytes, so this will corrupt every element.
     */
    const int step = 3;


    /* Copy the signature so that we can corrupt it */
    sig = OPENSSL_memdup(td->sig, td->siglen);
    if (sig == NULL)
        return 0;

    if (!TEST_ptr(pkey = hsspubkey_from_data(td->pub, td->publen))
        || !TEST_ptr(ctx = EVP_MD_CTX_create()))
        goto end;

    ret = 0;

    for (i = 0; i < (int)td->siglen; i += step) {
        sig[i] ^= 1; /* corrupt a byte */
        if (i > 0)
            sig[i - step] ^= 1; /* Reset the previously corrupt byte */

        /* Either the init or final are expected to fail */
        if (EVP_DigestVerifyPQInit(ctx, NULL, NULL, libctx, propq, pkey, NULL,
                                   sig, td->siglen) != 1)
            continue;
        if (!TEST_int_eq(EVP_DigestVerifyPQUpdate(ctx, td->msg, td->msglen), 1)
            || !TEST_int_ne(EVP_DigestVerifyPQFinal(ctx), 1))
            goto end;
    }

    ret = 1;
end:
    if (ret == 0)
        TEST_note("Incorrectly passed when %dth byte of signature"
                  " was corrupted", i);
    EVP_MD_CTX_free(ctx);
    EVP_PKEY_free(pkey);
    OPENSSL_free(sig);

    return ret == 1;
}

/*
 * Test that using the incorrect signature lengths (both shorter and longer)
 * fail.
 * NOTE: The we will not get an out of bounds read due to the signature
 * knowing how large it should be
 */
static int hss_verify_bad_sig_len_test(void)
{
    int ret = -1;
    LMS_VERIFY_DATA *td = &testdata[1];
    EVP_MD_CTX *ctx = NULL;
    EVP_PKEY *pkey = NULL;
    size_t siglen;
    const int step = 3;

    if (!TEST_ptr(pkey = hsspubkey_from_data(td->pub, td->publen))
        || !TEST_ptr(ctx = EVP_MD_CTX_create()))
        goto end;

    ret = 0;
    for (siglen = 0; siglen < td->siglen + 4 ; siglen += step) {
        if (siglen == td->siglen)   /* ignore the size that should pass */
            continue;
        if (EVP_DigestVerifyPQInit(ctx, NULL, NULL, libctx, propq, pkey, NULL,
                                   td->sig, siglen) == 1)
            goto end;
    }

    ret = 1;
end:
    if (ret == 0)
        TEST_note("Incorrectly accepted signature key of length"
                  " %ld (expected %ld)", siglen, td->siglen);
    EVP_MD_CTX_free(ctx);
    EVP_PKEY_free(pkey);

    return ret == 1;
}

