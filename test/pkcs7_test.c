 /* Helper function to load PKCS7 from placeholder data/file */
 static PKCS7 *load_pkcs7_data(const char *test_name, const char *filename)
 {
     BIO *bio = NULL;
     PKCS7 *p7 = NULL;
 
     /* Check for special value indicating embedded data */
     if (strcmp(filename, "EMBEDDED_SIG_DER") == 0) {
         bio = BIO_new_mem_buf(sig_der, sizeof(sig_der));
         if (bio == NULL) {
             TEST_error("Failed to create BIO for embedded sig_der");
             return NULL;
         }
         p7 = d2i_PKCS7_bio(bio, NULL);
     } else {
         /* Assume filename is a path to a DER file */
         bio = BIO_new_file(filename, "rb");
         if (bio == NULL) {
             TEST_info("Failed to open PKCS7 file '%s' for scenario '%s'. Skipping.", filename, test_name);
             /* Don't treat as fatal error, just skip scenario */
             return NULL;
         }
         p7 = d2i_PKCS7_bio(bio, NULL);
         if (p7 == NULL) {
             TEST_error("Failed to parse PKCS7 DER data from file '%s'", filename);
             /* Fall through to free bio */
         }
     }
 
     BIO_free(bio); /* Free BIO whether loading succeeded or failed */
     return p7;
 }
 
 /* Helper function to load Root CA from placeholder data/file */
 static X509 *load_root_ca_data(const char *test_name, const char *filename)
 {
     BIO *bio = NULL;
     X509 *cert = NULL;
 
     /* Check for special value indicating embedded data */
     if (strcmp(filename, "EMBEDDED_SMROOT_DER") == 0) {
         bio = BIO_new_mem_buf(smroot_der, sizeof(smroot_der));
         if (bio == NULL) {
             TEST_error("Failed to create BIO for embedded smroot_der");
             return NULL;
         }
         cert = d2i_X509_bio(bio, NULL); /* Load DER */
         if (cert == NULL)
             TEST_error("Failed to parse embedded smroot_der");
 
     } else if (strstr(filename, ".crt") != NULL || strstr(filename, ".pem") != NULL) {
         /* Assume PEM format for .crt or .pem files */
         bio = BIO_new_file(filename, "r");
         if (bio == NULL) {
             TEST_info("Failed to open Root CA file '%s' for scenario '%s'. Skipping.", filename, test_name);
             return NULL;
         }
         cert = PEM_read_bio_X509(bio, NULL, NULL, NULL);
         if (cert == NULL)
             TEST_error("Failed to parse Root CA PEM data from file '%s'", filename);
 
     } else if (strstr(filename, ".der") != NULL) {
         /* Assume DER format for .der files */
         bio = BIO_new_file(filename, "rb");
         if (bio == NULL) {
             TEST_info("Failed to open Root CA file '%s' for scenario '%s'. Skipping.", filename, test_name);
             return NULL;
         }
         cert = d2i_X509_bio(bio, NULL);
         if (cert == NULL)
             TEST_error("Failed to parse Root CA DER data from file '%s'", filename);
 
     } else {
         TEST_error("Unknown Root CA file format/extension for '%s'", filename);
         return NULL;
     }
 
     BIO_free(bio); /* Free BIO whether loading succeeded or failed */
     return cert;
 }
 
 
 /*
  * Test PKCS7_verify with policy flags (AND, REQUIRE_PQ).
  * Uses placeholder data for various scenarios.
  */
 static int pkcs7_verify_policy_test(void)
 {
     int ret = 1; /* Default to success, failures below will set to 0 */
     int test_idx, flag_idx;
     X509_STORE *store = NULL;
     X509 *rootcert = NULL;
     PKCS7 *p7 = NULL;
     int flags_to_test[] = {
         0,                                  /* Default (OR) */
         PKCS7_VERIFY_POLICY_AND,            /* AND */
         PKCS7_VERIFY_POLICY_REQUIRE_PQ,     /* REQUIRE_PQ */
         PKCS7_VERIFY_POLICY_AND | PKCS7_VERIFY_POLICY_REQUIRE_PQ /* AND | REQUIRE_PQ */
     };
     const char *flag_names[] = {"OR", "AND", "REQ_PQ", "AND|REQ_PQ"};
 
     for (test_idx = 0; test_idx < OSSL_NELEM(policy_test_scenarios); ++test_idx) {
         const PKCS7_POLICY_TEST_DATA *scenario = &policy_test_scenarios[test_idx];
         int scenario_ok = 1; /* Assume success for this scenario */
 
         TEST_info("--- Testing Scenario: %s ---", scenario->test_name);
 
         /* Load placeholder data */
         p7 = load_pkcs7_data(scenario->test_name, scenario->pkcs7_filename);
         rootcert = load_root_ca_data(scenario->test_name, scenario->root_ca_filename);
 
         /* If data is missing (placeholder), skip this scenario */
         if (p7 == NULL || rootcert == NULL) {
             TEST_info("Skipping scenario '%s' due to missing placeholder data.", scenario->test_name);
             X509_free(rootcert); /* Clean up if only one loaded */
             PKCS7_free(p7);      /* Clean up if only one loaded */
             /* We don't fail the overall test, just skip this scenario */
             continue;
         }
 
         /* Create cert store and add root */
         store = X509_STORE_new();
         if (!TEST_ptr(store) || !TEST_true(X509_STORE_add_cert(store, rootcert))) {
             TEST_error("Failed to create store or add root cert for scenario %s", scenario->test_name);
             scenario_ok = 0;
             goto scenario_end;
         }
 
         /* Set purpose to codeSigning as certificates were generated for it */
         X509_VERIFY_PARAM *param = X509_STORE_get0_param(store);
         if (!TEST_ptr(param)
             || !TEST_true(X509_VERIFY_PARAM_set_purpose(param, X509_PURPOSE_CODE_SIGN))
             || !TEST_true(X509_STORE_set1_param(store, param))) {
             TEST_error("Failed to set verification purpose for scenario %s", scenario->test_name);
             scenario_ok = 0;
             goto scenario_end;
         }
 
 
         for (flag_idx = 0; flag_idx < OSSL_NELEM(flags_to_test); ++flag_idx) {
             int current_flags = flags_to_test[flag_idx];
             int expected_result = scenario->expected_results[flag_idx];
             int expected_reason = scenario->expected_reasons[flag_idx];
             int verify_result;
             unsigned long err;
 
             TEST_info("Testing flags: %s (0x%x)", flag_names[flag_idx], current_flags);
             ERR_clear_error();
             verify_result = PKCS7_verify(p7, NULL, store, NULL, NULL, current_flags);
             err = ERR_peek_last_error(); /* Peek before TEST_true/false potentially adds errors */
 
             if (expected_result == 1) {
                 /* Expect SUCCESS */
                 if (!TEST_true(verify_result)) {
                     TEST_error("Scenario '%s', Flags '%s': Expected SUCCESS, got FAILURE",
                                scenario->test_name, flag_names[flag_idx]);
                     scenario_ok = 0;
                 }
             } else {
                 /* Expect FAILURE */
                 if (!TEST_false(verify_result)) {
                     TEST_error("Scenario '%s', Flags '%s': Expected FAILURE, got SUCCESS",
                                scenario->test_name, flag_names[flag_idx]);
                     scenario_ok = 0;
                 } else if (expected_reason != 0 && !TEST_int_eq(ERR_GET_REASON(err), expected_reason)) {
                     /* Check specific reason code if one is expected */
                     TEST_error("Scenario '%s', Flags '%s': Expected failure reason %d, got %d (Error: %lu)",
                                scenario->test_name, flag_names[flag_idx], expected_reason, ERR_GET_REASON(err), err);
                     scenario_ok = 0;
                 } else if (expected_reason == 0 && err == 0) {
                     /* Should have failed, but no error code set */
                      TEST_error("Scenario '%s', Flags '%s': Expected FAILURE, got FAILURE but no error code set",
                                scenario->test_name, flag_names[flag_idx]);
                     scenario_ok = 0;
                 }
             }
         } /* End flags loop */
 
     scenario_end:
         X509_STORE_free(store);
         store = NULL;
         X509_free(rootcert);
         rootcert = NULL;
         PKCS7_free(p7);
         p7 = NULL;
 
         if (!scenario_ok)
             ret = 0; /* Mark overall test as failed if any scenario check failed */
 
     } /* End scenario loop */
 
     return ret;
 }
 
 
