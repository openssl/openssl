/*
 * Internal functional + constant-time test for ossl_x25519
 * (X25519 ECDH shared-secret: out = scalar * peer_point).
 *
 * - Outside an enable-ct-validation build this is an ordinary functional KAT
 *   (RFC 7748 vectors).
 * - Under enable-ct-validation this suite additionally verifies that the
 *   scalar-multiplication ladder does not branch on, or index memory with,
 *   secret data.
 */

