/* The X25519 base point u = 9. */
static const uint8_t basepoint[32] = { 9 };

/*
 * Call ossl_x25519() to compute out = scalar * point.
 *
 * Taint targets:
 * - ossl_x25519 already marks the scalar (its private key) secret internally.
 * - Here we additionally over-taint the peer public point
 *   (the BoringSSL ctwrapX25519 pattern) to verify the choice of point cannot
 *   influence whether the scalar leaks.
 *
 * Stack copies and the output are declassified afterwards so process-wide
 * shadow memory does not stay tainted.
 */
static int derive(uint8_t out[32], const uint8_t scalar[32],
    const uint8_t point[32])
{
    uint8_t s[32], p[32];
    int ret;

    memcpy(s, scalar, sizeof(s));
    memcpy(p, point, sizeof(p));

    CONSTTIME_SECRET(s, sizeof(s));
    CONSTTIME_SECRET(p, sizeof(p));

    ret = ossl_x25519(out, s, p);

    CONSTTIME_DECLASSIFY(s, sizeof(s));
    CONSTTIME_DECLASSIFY(p, sizeof(p));
    CONSTTIME_DECLASSIFY(out, 32);
    return ret;
}

static int test_x25519_kat(void)
{
    uint8_t out[32];

    if (!TEST_int_eq(derive(out, in_scalar1, in_u1), 1)
        || !TEST_mem_eq(out, sizeof(out), out_u1, sizeof(out_u1)))
        return 0;

    if (!TEST_int_eq(derive(out, in_scalar2, in_u2), 1)
        || !TEST_mem_eq(out, sizeof(out), out_u2, sizeof(out_u2)))
        return 0;

    return 1;
}

static int test_x25519_dh(void)
{
    uint8_t out[32];

    /* Alice derives the shared secret from her private key and Bob's public. */
    if (!TEST_int_eq(derive(out, alice_priv, bob_pub), 1)
        || !TEST_mem_eq(out, sizeof(out), dh_shared, sizeof(dh_shared)))
        return 0;

    /* Bob derives the same secret from his private key and Alice's public. */
    if (!TEST_int_eq(derive(out, bob_priv, alice_pub), 1)
        || !TEST_mem_eq(out, sizeof(out), dh_shared, sizeof(dh_shared)))
        return 0;

    return 1;
}

/*
 * Exercise the Montgomery-ladder cswap across iterations with scalars that
 * stress the clamped-bit edges and alternating bit patterns.
 *
 * All scalars are multiplied by the prime-order base point, so every result is
 * non-zero (ossl_x25519 returns 1).
 *
 * The output bytes of ossl_x25519 are UNCHECKED. We only assert the call
 * succeeds and rely on CT validation to flag secret-dependent swaps.
 */
static int test_x25519_ladder_edges(void)
{
    static const uint8_t fills[] = { 0x00, 0xff, 0xaa, 0x55 };
    uint8_t scalar[32];
    uint8_t out[32];
    size_t i;

    /* Fill bytes. */
    for (i = 0; i < OSSL_NELEM(fills); i++) {
        memset(scalar, fills[i], sizeof(scalar));
        if (!TEST_int_eq(derive(out, scalar, basepoint), 1))
            return 0;
    }

    /* Single-bit edge pattern: Low bit set. */
    memset(scalar, 0, sizeof(scalar));
    scalar[0] = 0x01;
    if (!TEST_int_eq(derive(out, scalar, basepoint), 1))
        return 0;

    /* Single-bit edge pattern: High bit set. */
    memset(scalar, 0, sizeof(scalar));
    scalar[31] = 0x80;
    if (!TEST_int_eq(derive(out, scalar, basepoint), 1))
        return 0;

    return 1;
}

int setup_tests(void)
{
    ADD_TEST(test_x25519_kat);
    ADD_TEST(test_x25519_dh);
    ADD_TEST(test_x25519_ladder_edges);
    return 1;
}
