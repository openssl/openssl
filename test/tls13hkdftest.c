static int test_hkdf_case(SSL_CONNECTION *s, const struct hkdf_testvec *vec)
{
    const EVP_MD *md = EVP_get_digestbynid(vec->nid);
    unsigned char sentinel[EVP_MAX_MD_SIZE];
    unsigned char secret[EVP_MAX_MD_SIZE];
    unsigned char output[EVP_MAX_MD_SIZE];
    size_t prk_tail = sizeof(secret) - vec->prk_len;
    size_t out_tail = sizeof(output) - vec->expand_len;
    int ret;

    memset(sentinel, TEST_SENTINEL_BYTE, sizeof(sentinel));
    memset(secret, TEST_SENTINEL_BYTE, sizeof(secret));
    memset(output, TEST_SENTINEL_BYTE, sizeof(output));

    ret = tls13_hkdf_extract(s, md, vec->salt, vec->salt_len,
                             vec->ikm, vec->ikm_len, secret, 0);
    if (!TEST_true(ret)) {
        TEST_info("HKDF-Extract failed");
        return 0;
    }

    if (!TEST_mem_eq(secret, vec->prk_len, vec->prk, vec->prk_len)) {
        TEST_info("HKDF-Extract does not match");
        return 0;
    }
    if (!TEST_mem_eq(&secret[vec->prk_len], prk_tail, sentinel, prk_tail)) {
        TEST_info("HKDF-Extract overrun");
        return 0;
    }

    if (!vec->expand || !vec->expand_len || !vec->label)
        return 1;

    ret = tls13_hkdf_expand(s, md, secret,
                            vec->label, strlen(vec->label),
                            vec->hash, vec->hash_len,
                            output, vec->expand_len, 0);
    if (!TEST_true(ret)) {
        TEST_info("HKDF-Expand failed");
        return 0;
    }
    if (!TEST_mem_eq(output, vec->expand_len, vec->expand, vec->expand_len)) {
        TEST_info("HKDF-Expand does not match");
        return 0;
    }
    if (!TEST_mem_eq(&output[vec->expand_len], out_tail, sentinel, out_tail)) {
        TEST_info("HKDF-Expand overrun");
        return 0;
    }
    return 1;
}

static int test_hkdf_test_vectors(void)
{
    SSL_CTX *ctx = NULL;
    SSL *ssl = NULL;
    SSL_CONNECTION *s;
    int ret = 0;

    ctx = SSL_CTX_new(TLS_method());
    if (!TEST_ptr(ctx))
        goto err;

    ssl = SSL_new(ctx);
    if (!TEST_ptr(ssl) || !TEST_ptr(s = SSL_CONNECTION_FROM_SSL_ONLY(ssl)))
        goto err;

    s->session = SSL_SESSION_new();
    if (!TEST_ptr(s->session))
        goto err;

    for (int i = 0; i < sizeof(hkdf_vectors) / sizeof(hkdf_vectors[0]); i++) {
        ret = test_hkdf_case(s, &hkdf_vectors[i]);
        if (!TEST_true(ret)) {
            TEST_info("HKDF test vector failed: %s", hkdf_vectors[i].name);
            goto err;
        }
    }

    ret = 1;
 err:
    SSL_free(ssl);
    SSL_CTX_free(ctx);
    return ret;
}

int setup_tests(void)
{
    ADD_TEST(test_hkdf_test_vectors);
    return 1;
}
