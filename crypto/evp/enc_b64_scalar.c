int evp_encodeblock_int(EVP_ENCODE_CTX *ctx, unsigned char *t,
                        const unsigned char *f, int dlen, int *wrap_cnt)
{
    int i = 0;
    int ret = 0;
    uint8_t t1, t2, t3;
    const unsigned char *e0, *e1, *e2;
    int srp = (ctx != NULL
               && (ctx->flags & EVP_ENCODE_CTX_USE_SRP_ALPHABET) != 0);
    int wrap_cnt_by_input = *wrap_cnt / 4 * 3;
    const int ctx_length = (ctx != NULL) ? ctx->length : 0;

    if (srp) {
        e0 = base64_srp_bin2ascii_0;
        e1 = base64_srp_bin2ascii_1;
        e2 = base64_srp_bin2ascii_2;
    } else {
        e0 = base64_std_bin2ascii_0;
        e1 = base64_std_bin2ascii_1;
        e2 = base64_std_bin2ascii_2;
    }

    if (ctx_length == 1) {
        while (i < dlen && ret <= INT_MAX && ctx != NULL) {
            t1 = f[i];
            *(t++) = e0[t1];
            *(t++) = e1[(t1 & 0x03) << 4];
            *(t++) = '=';
            *(t++) = '=';
            *(t++) = '\n';

            ret += 5;
            i++;
        }

        *t = '\0';
        ret--;

        return ret;
    } else if (ctx_length % 3 != 0) {
        i = 0;
        int wrap_cnt_nm3 = 0;
        while (i + 2 < dlen && ret <= INT_MAX) {
            if (ctx != NULL) {
                if ((wrap_cnt_nm3 < ctx->length
                     && (wrap_cnt_nm3 + 3 + wrap_cnt_by_input) > ctx->length)
                    && ((ctx->flags & EVP_ENCODE_CTX_NO_NEWLINES) == 0)) {

                    switch (ctx->length % 3) {
                    case 0:
                        break;
                    case 1:
                        t1 = f[i];
                        *(t++) = e0[t1];
                        *(t++) = e1[(t1 & 0x03) << 4];
                        *(t++) = '=';
                        *(t++) = '=';

                        ret += 4;
                        i++;
                        break;
                    case 2:
                        t1 = f[i];
                        t2 = f[i + 1];
                        *(t++) = e0[t1];
                        *(t++) = e1[((t1 & 0x03) << 4) | ((t2 >> 4) & 0x0F)];
                        *(t++) = e2[(t2 & 0x0F) << 2];
                        *(t++) = '=';
                        i += 2;
                        ret += 4;
                        break;
                    }
                    *(t++) = '\n';
                    ret++;
                    wrap_cnt_nm3 = 0;
                }
            }

            if (ctx_length >= 4 && i + 2 < dlen) {
                t1 = f[i];
                t2 = f[i + 1];
                t3 = f[i + 2];
                *(t++) = e0[t1];
                *(t++) = e1[((t1 & 0x03) << 4) | ((t2 >> 4) & 0x0F)];
                *(t++) = e1[((t2 & 0x0F) << 2) | ((t3 >> 6) & 0x03)];
                *(t++) = e2[t3];
                ret += 4;
                wrap_cnt_nm3 += 3;
                i += 3;
            }
        }
    } else {
        for (i = 0; i + 2 < dlen && ret <= INT_MAX; i += 3) {

            t1 = f[i];
            t2 = f[i + 1];
            t3 = f[i + 2];
            *(t++) = e0[t1];
            *(t++) = e1[((t1 & 0x03) << 4) | ((t2 >> 4) & 0x0F)];
            *(t++) = e1[((t2 & 0x0F) << 2) | ((t3 >> 6) & 0x03)];
            *(t++) = e2[t3];
            ret += 4;

            if (ctx != NULL) {
                if ((i + 3 + wrap_cnt_by_input) % ctx->length == 0 &&
                    ((ctx->flags & EVP_ENCODE_CTX_NO_NEWLINES) == 0) &&
                    ctx->length % 3 == 0) {
                    *(t++) = '\n';
                    ret++;
                }
            }
        }
    }

    switch (dlen - i) {
    case 0:
        break;
    case 1:
        t1 = f[i];
        *(t++) = e0[t1];
        *(t++) = e1[(t1 & 0x03) << 4];
        *(t++) = '=';
        *(t++) = '=';

        ret += 4;

        if (ctx != NULL) {
            if ((i + 1 + wrap_cnt_by_input) % ctx->length == 0 &&
                ((ctx->flags & EVP_ENCODE_CTX_NO_NEWLINES) == 0) &&
                ctx->length % 3 == 0) {
                *(t++) = '\n';
                ret++;
            }
        }

        break;
    case 2:
        t1 = f[i];
        t2 = f[i + 1];
        *(t++) = e0[t1];
        *(t++) = e1[((t1 & 0x03) << 4) | ((t2 >> 4) & 0x0F)];
        *(t++) = e2[(t2 & 0x0F) << 2];
        *(t++) = '=';
        ret += 4;

        if (ctx != NULL) {
            if ((i + 2 + wrap_cnt_by_input) % ctx->length == 0 &&
                ((ctx->flags & EVP_ENCODE_CTX_NO_NEWLINES) == 0) &&
                ctx->length % 3 == 0) {
                *(t++) = '\n';
                ret++;
            }
        }
        break;
    }

    *t = '\0';

    return ret;
}
